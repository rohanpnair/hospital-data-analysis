plt.ylabel('AQI')
plt.grid(True)
plt.tight_layout()
plt.show()
plt.figure(figsize=(10, 5))
sns.histplot(df['PM2.5 AVG'], bins=30, kde=True, color='tomato')
plt.title('Distribution of PM2.5 Average Levels')
plt.xlabel('PM2.5 (Âµg/mÂ³)')
plt.ylabel('Frequency')
plt.tight_layout()
