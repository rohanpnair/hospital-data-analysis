import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns
print("âœ… Pollution analysis script is running...")
df=pd.read_csv("HDHI Pollution Data.csv")
print("\nðŸ“Š Pollution Data columns:")
print(df.columns)
df['DATE']=pd.to_datetime(df['DATE'],dayfirst=True,errors='coerce')
plt.figure(figsize=(12,6))
